package com.mykvantorium.ui.progress;

public class ProgressModel {
    String date;
    int score;

    public ProgressModel() {
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public Integer getScore() {
        return score;
    }

    public void setScore(Integer score) {
        this.score = score;
    }
}
