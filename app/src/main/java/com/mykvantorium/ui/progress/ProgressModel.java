package com.mykvantorium.ui.progress;

public class ProgressModel {
    String date, score;

    public ProgressModel() {
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }
}
